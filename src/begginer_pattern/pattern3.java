package begginer_pattern;

public class pattern3 {
    public static void main(String[] args){
        for(int i=0;i<5;i++){
            for(int j=0;j<i+1;j++){
                System.out.print((j+1)+" ");
            }
            System.out.println();
        }
    }
}


